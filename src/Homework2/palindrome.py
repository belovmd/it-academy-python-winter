"""
- Определите, является ли число палиндромом (читается слева направо
и справа налево одинаково).  Число положительное целое, произвольной
длины. Задача требует работать только с числами (без конвертации числа
в строку)
"""
num = int(input('Введите целое число\n'))
copy_num = num
revers_num = 0
while copy_num:
    revers_num = revers_num * 10 + copy_num % 10
    copy_num //= 10
if num == revers_num:
    print('Это палиндром!')
else:
    print('Это не палиндром!')
